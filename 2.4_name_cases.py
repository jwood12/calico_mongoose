# 2-4. 11/24/2023 Name Cases: Use a variable to represent a person's name, and then print that person's name in lowercase, uppercase, and title case.

#lowercase
first_name = "JIMmy" 
last_name = "wOOd"
full_name = f"{first_name} {last_name}"
print(f"{full_name.lower()}")

#uppercase
first_name = "JIMmy" 
last_name = "wOOd"
full_name = f"{first_name} {last_name}"
print(f"{full_name.upper()}")

#title case
first_name = "JIMmy" 
last_name = "wOOd"
full_name = f"{first_name} {last_name}"
print(f"{full_name.title()}")
